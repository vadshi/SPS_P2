class Point:
    def __init__(self, x, y):
        self.x = x
        self.y = y


point1 = Point(10, -8)
point2 = Point(12, 5)

print(type(point1))  # <class '__main__.Point'>
print(type(point2))  # <class '__main__.Point'>

# globals() вернет словарь глобальных переменных
